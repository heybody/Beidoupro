/*************************************************************************************************************
*                                            FUNCTION PROTOTYPES                                             *
*************************************************************************************************************/
void DevSleep (DevSleepTypeDef sleep)
{
    switch (sleep)
    {
        case DEV_SLEEP_RUN:     EMURestore();
                                break;
        case DEV_SLEEP_NORMAL:  EMUEnterEM1();
                                break;
        case DEV_SLEEP_DEEP:    EMUEnterEM2();
                                break;
        case DEV_SLEEP_STOP:    EMUEnterEM3();
                                break;
        case DEV_SLEEP_SHUTOFF: EMUEnterEM4();
                                break;
    }

}


