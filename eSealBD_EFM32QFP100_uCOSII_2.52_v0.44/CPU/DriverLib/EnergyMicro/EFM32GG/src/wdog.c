#include <efm32.h>


/*************************************************************************************************************
*                                            FUNCTION PROTOTYPES                                             *
*************************************************************************************************************/
void WDOGClockEnable (WDOGTypeDef n)
{
    switch (n)
    {
        case WDOG_0:   CMUHFCOREClockEnable(LEClock);
                        break;
        default:        break;     
    }  
}

void WDOGClockDisable (WDOGTypeDef n)
{
    switch (n)
    {
        case WDOG_0:   CMUHFCOREClockDisable(LEClock);
                        break;
        default:        break;     
    }  
}

